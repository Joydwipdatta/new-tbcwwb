            <h1>FAQ</h1>
            <div class="faq-box">
                
                <button class="accordion">
                    Q21 Is there any establishment which does not come under the purview of this Building & 
                    Other Construction Workers’ Welfare Cess [BOCWW Cess]?
                </button>
                <div class="panel">
                    
                    <p>
                        Yes. According to the definition of “establishment” as per Sec. 2(j) of the Building & Other 
                        Construction Workers’ (Regulation of Employment & Condition of Service) Act, 1996, the 
                        mother Act – “establishment” does not include an individual who employs such workers in 
                        any building or contraction work in relation to his own residence where the total cost of such 
                        construction not being more than rupees ten lakhs. 
                    </p>
                </div>
                <button class="accordion">
                    Q22 Is it mandatory to pay Building & Other Construction Workers’ Welfare Cess [BOCWW 
                    Cess] is payable suo-motu as an advance in every case?
                </button>
                <div class="panel">
                    
                    <p>
                        As per the provisions of Section 3(2) of the Building and Other Construction Workers’ 
                        Welfare Cess Act, 1996, an employer may pay advance Cess on the basis of estimated cost of 
                        construction along with notice of commencement of work.  
                    </p>
                    <p>
                        If the construction exceeds one year the final Cess payment is to be made after the total 
                        completion of work.
                    </p>
                </div>
                <button class="accordion">
                    Q23 Does the onus of Building & Other Construction Workers’ Welfare Cess [BOCWW Cess]
                    falls on the Government or the owner of a building which is being constructed only? 
                    Can the contractor undertaking the job on being instructed by the Govt. be held 
                    responsible anyhow for paying the Building & Other Construction Workers’ Welfare 
                    Cess [BOCWW Cess]?
                </button>
                <div class="panel">
                    
                    <p>
                        Yes. The liability to pay Building & Other Construction Workers’ Welfare Cess [BOCWW 
                        Cess] falls not only on the owner of a building or establishment, but on the contractor also 
                        under specific conditions.
 
                    </p>
                    <p>
                        Under the provisions of Section 2(1)(i)(iii) of the Building and Other Construction Workers’ 
                        Welfare Cess Act, 1996, the contractor is Employer, i.e. the person responsible to pay 
                        Building & Other Construction Workers’ Welfare Cess [BOCWW Cess], in relation to a 
                        building or other construction work carried on by or through a contractor, or by the 
                        employment of building workers supplied by a contractor.
                    </p>
                    <p>
                        In this context, the Hon’ble Supreme Court in the case Dewan Chand Builders and 
                        Contractors [(2012) 1 SCC 101], has mentioned that “The extension of the liability on to the 
                        contractor is with a view to ensure that, if for any reason it is not possible to collect Cess from 
                        the owner of the building at a stage subsequent to the completion of the construction, it can be 
                        recovered from the contractor. The Cess Act and the Cess Rules ensure that the Cess is 
                        collected at source from the bills of the contractors to whom payments are made by the owner. In short, the burden of Cess is passed on from the owner to the contractor.”
                    </p>
                </div>
                <button class="accordion">
                    Q24 What is the scope and meaning of contractor in the Building & Other Construction 
                    Workers’ Welfare Cess [BOCWW Cess] laws?
                </button>
                <div class="panel">
                    
                    <p>
                        As per Section 2(1)(g) of the Building and Other Construction Workers (Regulation of 
                        Employment and Conditions of Service) Act, 1996 read with Section 2(d) of the Building and 
                        Other Construction Workers’ Welfare Cess Act, 1996, the term “contractor” means a person 
                        who undertakes to produce a given result for any establishment, other than a mere supply of 
                        goods or articles of manufacture, by the employment of building workers or who supplies 
                        building workers for any work of the establishment, and includes a sub-contractor.
 
                    </p>
                    <p>
                        Thus, it is needless to say, that the scope of liability to pay Building & Other Construction 
                        Workers’ Welfare Cess [BOCWW Cess] extends to not only a contractor but also to a sub-contractor.
                    </p>
                </div>
                <button class="accordion">
                    Q25 The Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] appears to be 
                    a Central Legislation. In that case is the State Government mandatorily bound to 
                    ensure the implement such law?

                </button>
                <div class="panel">
                    
                    <p>
                        As per Section 2(1)(a)(iii) of the Building and Other Construction Workers (Regulation of 
                        Employment and Conditions of Service) Act, 1996 read with Section 2(d) of the Building and 
                        Other Construction Workers’ Welfare Cess Act, 1996, the term “appropriate Government” 
                        means in relation to any other establishment which employs building workers either directly 
                        or through a contractor, the Government of the State in which that other establishment 
                        is situated.
 
                    </p>
                    <p>
                        Thus, the Government of a State is also a stakeholder in implementation of this statute.
                    </p>
                </div>
                <button class="accordion">
                    Q26 What are the activities on which Building & Other Construction Workers’ Welfare Cess 
[                   BOCWW Cess] is applicable?

                </button>
                <div class="panel">
                    
                    <p>
                        Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] is levied on the cost of 
                        construction incurred by the employers on the building and other construction works.
                    </p>
                    <p>
                        As per Section 2(1)(d) of the Building and Other Construction Workers (Regulation of 
                        Employment and Conditions of Service) Act, 1996 read with Section 2(d) of the Building and 
                        Other Construction Workers’ Welfare Cess Act, 1996, the term “building or other 
                        construction work” has a very wide coverage. 
                    </p>
                    <p>
                        It includes the construction, alteration, repairs, maintenance or demolition of or, in relation 
                        to, buildings, streets, roads, railways, tramways, airfields, irrigation, drainage, embankment 
                        and navigation works, flood control works (including storm water drainage works), 
                        generation, transmission and distribution of power, water works (including channels for 
                        distribution of water), oil and gas installations, electric lines, wireless, radio; television, 
                        telephone, telegraph and overseas communication dams, canals, reservoirs, watercourses, tunnels, bridges, viaducts, aquaducts, pipelines, towers, cooling towers, transmission towers 
                        and such other work as may be specified in this behalf by the appropriate Government, by 
                        notification but does not include any building or other construction work to which the 
                        provisions of the Factories Act, 1948, or the Mines Act, 1952, apply.
                    </p>
                </div>
                <button class="accordion">
                    Q27 Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] is paid by an 
                    employer of the labours engaged in building construction. What is the scope and 
                    meaning of such employer?
                </button>
                <div class="panel">
                    
                    <p>
                        As per Section 2(1)(i) of the Building and Other Construction Workers (Regulation of 
                        Employment and Conditions of Service) Act, 1996 read with Section 2(d) of the Building and 
                        Other Construction Workers’ Welfare Cess Act, 1996, an “employer” in relation to an 
                        establishment, means the owner thereof, and includes,—
                    </p>
                    <p>
                        1) in relation to a building or other construction work carried on by or under the 
                        authority of any department of the Government, directly without any contractor, the 
                        authority specified in this behalf, or where no authority is specified, the head of the 
                        department; 
                    </p>
                    <p>
                        2) in relation to a building or other construction work carried on by or on behalf of a 
                        local authority or other establishment, directly without any contractor, the chief 
                        executive officer of that authority or establishment;
                    </p>
                    <p>
                        3) in relation to a building or other construction work carried on by or through a 
                        contractor, or by the employment of building workers supplied by a contractor, the 
                        contractor.                        
                    </p>
                </div>
                <button class="accordion">
                    Q28 As per the statute, all the Govt. Departments are required to collect Building & Other 
                    Construction Workers’ Welfare Cess [BOCWW Cess] for building & other construction 
                    works of establishments from employer-contractors. What is meant by the term 
                    “establishment”?

                </button>
                <div class="panel">
                    
                    <p>
                        As per Section 2(1)(j) of the Building and Other Construction Workers (Regulation of 
                        Employment and Conditions of Service) Act, 1996 read with Section 2(d) of the Building and 
                        Other Construction Workers’ Welfare Cess Act, 1996, an “establishment” means any 
                        establishment belonging to, or under the control of, Government, any body-corporate or firm, 
                        an individual or association or other body of individuals which or who employs building 
                        workers in any building or other construction work; and includes an establishment 
                        belonging to a contractor, but does not include an individual who employs such workers in 
                        any building or contraction work in relation to his own residence the total cost of such 
                        construction not being more than rupees ten lakhs.
                    </p>
                </div>
                <button class="accordion">
                    Q29 Consider the following cases:
                </button>
                <div class="panel">
                    
                    <p>
                        1) PWD, G/WB undertakes the renovation of the offices building of the Directorate 
                        of Commercial Taxes and engages a contractor M/s X to do the same.
                    </p>
                    <p>2) Housing Dept, G/WB engages a contractor to construct residential buildings to 
                        be handed over to applicants in lease.</p>
                    <p>
                        3) I&W Dept, G/WB undertakes an extensive area development project of digging of 
                        new irrigation canals, constructing embankments on the river and digging of 
                        water-bodies.
                    </p>
                    <p>
                        4) NH Division of PW (Roads), G/WB undertakes the project of extension of the 
                        existing Highway and addition of multi-lanes therein.
                    </p>
                    <p>
                        5) Health Dept, G/WB makes necessary alterations in an indoor stadium to convert 
                        it into a quarantine centre to fight pandemic.
                    </p>
                    <p>
                        6) P&RD Dept, G/WB constructs a new bridge thus connecting two blocks across 
                        the river.
                    </p>
                    <p>
                        7) Education Dept, G/WB completes the project of interior decoration of its 
                        District offices
                    </p>
                    <p>
                        8) PHE Dept, G/WB undertakes a project for supply of safe drinking water based on 
                        Surface Water.
                    </p>
                    <p>
                        Now, when the payments will be made to the beneficiaries for such work done, will 
                        Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] be deducted on 
                        the Bill amount?
                    </p><br>
                    <p><strong>ans:</strong>  </p>
                    <p>
                        As already discussed above, as per Section 2(1)(d) of the Building and Other Construction 
                        Workers (Regulation of Employment and Conditions of Service) Act, 1996 read with Section 
                        2(d) of the Building and Other Construction Workers’ Welfare Cess Act, 1996, the term 
                        “building or other construction work” has a very wide coverage. 
                    </p>
                    <p>It includes activities like</p>
                    <p>1)  construction,</p>
                    <p>2) alteration,</p>
                    <p>3) repairs,</p>
                    <p>4) maintenance and even</p>
                    <p>5) demolition</p>
                    <p>
                        of or, in relation to, a wide area of works like: 
                    </p>
                    <p>
                        buildings, streets, roads, railways, tramways, airfields, irrigation, drainage, embankment 
                        and navigation works, flood control works (including storm water drainage works), 
                        generation, transmission and distribution of power, water works (including channels for 
                        distribution of water), oil and gas installations, electric lines, wireless, radio; television, 
                        telephone, telegraph and overseas communication dams, canals, reservoirs, watercourses, 
                        tunnels, bridges, viaducts, aquaducts, pipelines, towers, cooling towers, transmission towers 
                        and such other work as may be specified in this behalf by the appropriate Government, by 
                        notification.
                    </p>
                    <p>
                        The only area of exclusion is any building or other construction work to which the provisions 
                        of the Factories Act, 1948, or the Mines Act, 1952, apply.      
                    </p>
                    <p>
                        It may be stated that the term “of” as well as “in relation to” has a very wide range of 
                        meaning.
                    </p>
                    <p>
                        The Hon’ble Supreme Court has thrown some light on the meaning of the word “in relation 
                        to” and opined that the term “in relation to” is a very broad expression which pre-supposes 
                        another subject matter. These are words of comprehensiveness which might both have a 
                        direct significance as well as indirect significance depending on the context.
                    </p>
                    <p>
                        In Doypack Systems Pvt. Ltd. vs. Union of India and Ors. [1988 AIR 782, 1988 SCR (2) 962]
                        the Court has observed: "The expressions 'pertaining to', 'in relation to' and 'arising out of', 
                        used in the deeming provision, are used in the expansive sense. The expression 'arising out 
                        of' has been used in the sense that it comprises purchase of shares and lands from income 
                        arising out of the Kanpur Undertaking. The words "pertaining to" and "in relation to" have 
                        the same wide meaning and have been used interchangeably for among other reasons, which 
                        may include avoidance of repetition of the same phrase in the same clause or sentence, a 
                        method followed in good drafting. The word 'pertain' is synonymous with the word 'relate'. 
                        The term 'relate' is also defined as meaning to bring into association or connection with. The 
                        expression 'in relation to' (so also 'pertaining to'), is a very broad expression which 
                        presupposes another subject matter. These are words of comprehensiveness which might 
                        have both a direct significance as well as an indirect significance depending on the context." 
                    </p>
                    <p>
                        In Home Solution Retail India Ltd vs. UOI & Ors [2009(237) ELT 209 (Del.)], the Court has 
                        observed: “The phrase ‘in relation to’ has been construed by this Court to be of the widest 
                        amplitude.”
                    </p>
                    <p>
                        Thus it may appear that the terms “in relation to” are subject to interpretations.
                    </p>
                    <p>
                        But, in the given cases (i – vii) all the activities and the areas of such work appear to be 
                        explicitly covered by the given definition of building or other construction as per Section 
                        2(1)(d) of the Building and Other Construction Workers (Regulation of Employment and 
                        Conditions of Service) Act, 1996 read with Section 2(d) of the Building and Other 
                        Construction Workers’ Welfare Cess Act, 1996.
                    </p>
                    <p>
                        Thus, in absence of anything to the contrary at this instance, it appears that Building & 
                        Other Construction Workers’ Welfare Cess [BOCWW Cess] is required to be deducted by the 
                        DDO while making payments to the beneficiaries in all the above cases.
                    </p>
                </div>
                <button class="accordion">
                    Q30 What is the methodology of payment of the beneficiaries in all the above cases by a 
                    DDO of a WB Govt. Dept. for the purpose of deducting Building & Other Construction 
                    Workers’ Welfare Cess [BOCWW Cess]?
                </button>
                <div class="panel">
                    
                    <p>
                        As it has already been discussed, the term “building or other construction work” has a very 
                        large ambit covering almost every area of supply where both goods and services are involved 
                        simultaneously.
                    </p>
                    <p>
                        As evident from the nature of such work, the payments for any such work when undertaken 
                        by the Govt. is made through the IFMS e-billing module.
                    </p>
                    <p>
                        The same should be done in these cases too where deduction of Building & Other 
                        Construction Workers’ Welfare Cess [BOCWW Cess] is involved. 
                    </p>
                </div>

