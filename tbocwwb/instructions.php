        <div class="instruction">
            <h2><span> Section 1 </span> Short title, extent and commencement.</h2>
            <p>(1) This Act may be called the Building and Other Construction Workers Welfare Cess Act, 1996.</p>
            <p>  (2) It extends to the whole of India.</p>
            <p>(3) It shall be deemed to have come into force on the 3rd day of November, 1995.</p> 
           
        </div>
        <div class="instruction">
            <h2><span> Section 2 </span>  Definitions.</h2>
            <h4>In this Act, unless the context otherwise requires,--</h4>
            <p> (a) "Board" means a Building and Other Construction Workers' Welfare Board constituted by a State Government under sub-section (1) of section 18 of the Building and Other Construction Workers (Regulation of Employment and Conditions of Service) Act, 1996 (27 of 1996);</p>
            <p>(b) "Fund" means the Building and Other Construction Workers' Welfare Fund constituted by a Board;</p> 
            <p>(c) "prescribed" means prescribed by rules made under this Act;</p>
            <p>(d) words and expressions used herein but not defined and defined in the Building and Other Construction Workers (Regulation of Employment and Conditions of Service) Act, 1996 (27 of 1996) shall have the meanings respectively assigned to them in that Act.  </p>
        </div>
        <div class="instruction">
            <h2><span> Section 3 </span> Levy and collection of cess.</h2>
            <p>(1) There shall be levied and collected a cess for the purposes of the Building and Other Construction Workers (Regulation of Employment and Conditions of Service) Act, 1996 (27 of 1996), at such rate not exceeding two per cent. but not less than one per cent. of the cost of construction incurred by an employer, as the Central Government may, by notification in the Official Gazette, from time to time specify.</p>
            <p> (2) The cess levied under sub-section (1) shall be collected from every employer in such manner and at such time, including deduction at source in relation to a building or other construction work of a Government or of a public sector undertaking or advance collection through a local authority where an approval of such building or other construction work by such local authority is required, as may be prescribed.</p>
            <p>(3) The proceeds of the cess collected under sub-section (2) shall be paid by the local authority or the State Government collecting the cess to the Board after deducting the cost of collection of such cess not exceeding one per cent. of the amount collected.</p> 
            <p>(4) Notwithstanding anything contained in sub-section (1) or sub-section (2), the cess leviable under this Act including payment of such cess in advance may, subject to final assessment to be made, be collected at a uniform rate or rates as may be prescribed on the basis of the quantum of the building or other construction work involved.</p>
            <h3>STATE AMENDMENTS</h3>
            <h4>Uttar Pradesh.--</h4>
            <p> <b>Amendment of section 3 of Act no. 28 of 1996.--</b> In section 3 of the Building and Other Construction Workers Welfare Cess Act, 1996, in sub-section (1) the following explanation shall be inserted at the end, namely:--</p>
            <p> <b> "Explanation:--</b> For the purpose of levy of cess under this sub-section cost of construction shall include all expenditure incurred by an employer in connection with the building or other construction work but shall not include--</p>
            <p>(a) cost of land;</p>
            <p>(b) any compensation paid or payable to a worker or his dependents under the Employees's Compensation Act, 1923;</p>
            <p>(d) machines such as MRI, CT Scan, Dialysis machine etc. used for treatment of patients in hospitals."</p>
            <br><p>[Vide the Uttar Pradesh Act 28 of 2018, s. 2]</p>
        </div>
        

