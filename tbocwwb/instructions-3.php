        <div class="instruction">
            <h2><span> Section 9 </span> Penalty for non-payment of cess within the specified time.</h2>
            <p>If any amount of cess payable by any employer under section 3 is not paid within the date specified in the order of assessment made under section 5, it shall be deemed to be in arrears and the authority prescribed in this behalf may, after making such inquiry as it deems fit, impose on such employer a penalty not exceeding the amount of cess:</p>
            <p>Provided that, before imposing any such penalty, such employer shall be given a reasonable opportunity of being heard and if after such hearing the said authority is satisfied that the default was for any good and sufficient reason, no penalty shall be imposed under this section.</p>
        </div>
        <div class="instruction">
            <h2><span> Section 10 </span>   Recovery of amount due under the Act.</h2>
            <p>Any amount due under this Act (including any interest or penalty) from an employer may be recovered in the same manner as an arrear of land revenue.</p>
        </div>
        <div class="instruction">
            <h2><span> Section 11 </span> Appeals.</h2>
            <p>(1) Any employer aggrieved by an order of assessment made under section 5 or by an order imposing penalty made under section 9 may, within such time as may be prescribed, appeal to such appellate authority in such form and in such manner as may be prescribed.</p>
            <p>(2) Every appeal preferred under sub-section (1) shall be accompanied by such fees as may be prescribed.</p>
            <p>(3) After the receipt of any appeal under sub-section (1), the appellate authority shall, after giving the appellant an opportunity of being heard in the matter, dispose of the appeal as expeditiously as possible.</p>
            <p>(4) Every order passed in appeal under this section shall be final and shall not be called in question in any court of law.</p>
        </div>
        <div class="instruction">
            <h2><span> Section 12 </span>  Penalty.</h2>
            <p>(1) Whoever, being under an obligation to furnish a return under this Act, furnishes any return knowing, or having reason to believe, the same to be false shall be punishable with imprisonment which may extend to six months, or with fine which may extend to one thousand rupees, or with both.</p>
            <p>(2) Whoever, being liable to pay cess under this Act, wilfully or intentionally evades or attempts to evade the payment of such cess shall be punishable with imprisonment which may extend to six months, or with fine, or with both.</p>
            <p>(3) No court shall take cognizance of an offence punishable under this section save on a complaint made by or under the authority of the Central Government.</p>
        </div>
        <div class="instruction">
            <h2><span> Section 13 </span>   Offences by companies.</h2>
            <p>(1) Where an offence under this Act has been committed by a company, every person who, at the time the offence was committed, was in charge of, and was responsible to, the company for the conduct of the business of the company, as well as the company, shall be deemed to be guilty of the offence and shall be liable to be proceeded against and punished accordingly:</p>
            <p>Provided that nothing contained in this sub-section shall render any such person liable to any punishment if he proves that the offence was committed without his knowledge or that he had exercised all due diligence to prevent the commission of such offence.</p>
            <p>(2) Notwithstanding anything contained in sub-section (1), where an offence under this Act has been committed with the consent or connivance of, or is attributable to any neglect on the part of, any director, manager, secretary or other officer of the company, such director, manager, secretary or other officer shall also be deemed to be guilty of that offence and shall be liable to be proceeded against and punished accordingly.</p>
            <p>Explanation.--For the purposes of this section,--</p>
            <p>(a) "company" means any body corporate and includes a firm or other association of individuals; and</p>
            <p>(b) "director", in relation to a firm, means a partner in the firm.</p>
        </div>

