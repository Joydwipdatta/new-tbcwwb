        <div class="body-box">
            <h1>FAQ</h1>
            <div class="faq-box">
                <button class="accordion">
                    Q1 What is Building & Other Construction Workers’ Welfare Cess [BOCWW Cess]?
                </button>
                <div class="panel">
                    <p>
                        Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] is a means to provide 
                        health and welfare measures for the workers engaged in building and other construction 
                        works.
                       
                        
                    </p>
                    <p>
                        Building and other construction workers are one of the most numerous and vulnerable 
                        segments of the unorganized labour in India. The building and other construction works are 
                        characterised by their inherent risk to the life and limb of the workers. The work is also 
                        characterised by its casual nature, temporary relationship between employer and employee, 
                        uncertain working hours, lack of basic amenities and inadequacy of welfare facilities.
                    </p>
                    <p>
                        That is why; a need has been felt for a comprehensive statute for regulating their safety, 
                        health, welfare and other conditions of service.
                    </p>
                </div>

                <button class="accordion">
                    Q2 What is Labour Cess?
                </button>
                <div class="panel">
                    <p>
                        In the truest sense, there is no cess called Labour Cess. 
                    </p>
                    <p>
                        
                        It is the Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] which is often 
                        referred as labour cess by certain section/ individual.

                    </p>
                </div>

                <button class="accordion">
                    Q3 How is this Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] enacted?
                </button>
                <div class="panel">
                    <p>
                        Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] is administered under 
                        the Building and Other Construction Workers’ Welfare Cess Act, 1996 read with relevant 
                        sections of the Building and Other Construction Workers (Regulation of Employment and 
                        Conditions of Service) Act, 1996.
                    </p>
                </div>
                <button class="accordion">
                    Q4 What is the constitutional foothold of the enactment of the Building & Other 
                    Construction Workers’ Welfare Cess [BOCWW Cess]?
                </button>
                <div class="panel">
                    <p>
                        The Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] under the 
                        provisions of the Building and Other Construction Workers’ Welfare Cess Act, 1996 is a levy 
                        covered under Entry 97 of List – I, Union List, read with Entry at serial 23 and 24 of List III 
                        in Schedule VII. 
                    </p>
                    <p>
                        The same has been upheld in the case of Dewan Chand Builders and Contractors v. Union of 
                        India and Other [(2012) 1 SCC 101] where the Hon’ble Supreme Court has said that the levy 
                        under the Building and Other Construction Workers’ Welfare Cess Act, 1996 is a “fee” referable to Schedule VII of List I Entry 97 of the Constitution of India.

                    </p>
                </div>
                <button class="accordion">
                    Q5 Is this cess another tax levied on the labour portion of a contract? 
                </button>
                <div class="panel">
                    <p>
                        No. The levy of Cess on the cost of construction incurred by the employers on the building 
                        and other construction works is a means for ensuring sufficient funds for the Welfare Boards 
                        to undertake social security schemes and welfare measures for building and other 
                        construction workers.
                    </p>
                    <p>
                        In this connection, the Hon’ble Supreme Court in the case of Dewan Chand Builders and 
                        Contractors v. Union of India and Other [(2012) 1 SCC 101] has said that:
                    </p>
                    <p>
                        “The fund, so collected, is directed to specific ends spelt out in the BOCW Act. Therefore, 
                        applying the principle laid down in the aforesaid decisions of this Court, it is clear that the 
                        said levy is a `fee' and not `tax'. The said fund is set apart and appropriated specifically for 
                        the performance of specified purpose; it is not merged in the public revenues for the benefit 
                        of the general public and as such the nexus between the Cess and the purpose for which it is 
                        levied gets established, satisfying the element of quid pro quo in the scheme. With these 
                        features of the Cess Act in view, the subject levy has to be construed as `fee' and not a `tax'.
                    </p>
                </div>
                <button class="accordion">
                    Q6 What is the difference between a tax and fee in connection with cess?
                </button>
                <div class="panel">
                    <p>
                        In the case of State of WB vs. Kesoram Industries Ltd. & Ors. [(2004) 10 SCC 201], the 
                        Constitution Bench of the Supreme Court, was faced with a challenge to the Constitutional 
                        validity of the levy of Cesses on coal-bearing lands, tea plantation lands and on removal of 
                        bricks earth. 
                    </p>
                    <p>
                        Relying on the decision in Hingir Rampur Coal Co. Ltd (AIR 1961 SC 459), speaking for the 
                        majority, R.C. Lahoti, J. (as His Lordship then was), explained the distinction between the 
                        terms `tax' and `fee' in the following words: "…The term cess is commonly employed to 
                        connote a Tax with a purpose or a tax allocated to a particular thing. However, it also means 
                        an assessment or levy. Depending on the context and purpose of levy, cess may not be a tax; 
                        it may be a fee or fee as well. 
                    </p>
                    <p>
                        It is not necessary that the services rendered from out of the Fee collected should be directly 
                        in proportion with the amount of Fee collected.
                    </p>
                    <p>
                        It is equally not necessary that the services rendered by the Fee collected should remain 
                        confined to the person from whom the fee has been collected.
                    </p>
                    <p>
                        Availability of indirect benefit and a general nexus between the persons bearing the burden 
                        of levy of fee and the services rendered out of the fee collected is enough to uphold the 
                        validity of the fee charged." 
                    </p>
                </div>
                <button class="accordion">
                    Q7 What is the controlling body for ensuring the provision of health and welfare measures 
                    to the workers engaged in building and other construction works?
                </button>
                <div class="panel">
                    <p>
                        The Welfare Boards constituted under Section 18(1) of the Building and Other Construction 
                        Workers (Regulation of Employment and Conditions of Service) Act, 1996 are entrusted with 
                        the task of ensuring the provision of health and welfare measures to the workers engaged in 
                        building and other construction works of the Building and other construction Workers.
                    </p>
                    <p>
                        In West Bengal, such functions are carried out by the West Bengal Building and Other 
                        Construction Workers’ Welfare Board (WBBOCWW Board) constituted vide Notification No. 
                        1182-I.R/ EIL/ 1A-18/ 2004(Pt) Dt. 20.09.2005 of the Labour Department, Govt. of West 
                        Bengal.
                    </p>
                    <p>
                        Reconstituted latest by Notification No. Labr/895/ (LC-IR)/LW/3A-1/16 Dt. 31.08.2016.
                    </p>
                </div>
                <button class="accordion">
                    Q8 What is the name of the Fund created for ensuring the provision of health and welfare 
                    measures to the workers engaged in building and other construction works?
                </button>
                <div class="panel">
                    <p>
                        The “Building and Other Construction Workers’ Welfare Fund” created under the provisions 
                        of Section 24 of the Building and Other Construction Workers (Regulation of Employment 
                        and Conditions of Service) Act, 1996 provides for health and welfare measures to the workers 
                        engaged in building and other construction works.
                    </p>
                    <p>
                        In the State of West Bengal, the same functions are carried out through the “West Bengal 
                        Building and Other Construction Workers’ Welfare Fund”.
                    </p>
                    
                </div>
                <button class="accordion">
                    Q9 What is the source of this Welfare Fund?
                </button>
                <div class="panel">
                    <p>
                        The source of this Fund is the Building & Other Construction Workers’ Welfare Cess 
                        [BOCWW Cess] levied and collected on the cost of construction incurred by employers, under 
                        Section 3 of the Building and Other Construction Workers’ Welfare Cess Act, 1996. This 
                        Cess augments the resources of the Building and Other Construction Workers’ Welfare 
                        Boards constituted by the Government (Central, State & Union Territory).
                    </p>
    
                </div>
                <button class="accordion">
                    Q10 Should one take a Registration under BOCWW Cess Act, 1996?
                </button>
                <div class="panel">
                    <p>
                        No, there is no registration process associated to BOCWW Cess Act, 1996.
                    </p>
                    <p>
                        However, under Sec. 7 of the Building & Other Construction Workers’ (Regulation of 
                        Employment & Condition of Service) Act, 1996 there lies a provision for registration of 
                        establishment where the employer employs, or had employed on any day of the preceding 
                        twelve months, ten or more building workers in any building or other construction work. The 
                        employer shall apply for registration within a period of sixty days from commencement of 
                        such construction work.
                    </p>
                    <p>
                        Thus, it is evident that this is a liability on the part of an employer (i.e. contractor) and not 
                        that of the Govt. Dept. engaging a contractor for building & other constructions.
                    </p>
                </div>
                
                
                
