    <section class="in-section">
        <div class="instruction">
            <h2><span> Section 4 </span> Furnishing of returns.</h2>
            <p>(1) Every employer shall furnish such return to such officer or authority, in such manner and at such time as may be prescribed.</p>
            <p>  (2) If any person carrying on the building or other construction work, liable to pay the cess under section 3, failsto furnish any return under sub-section (1), the officer or the authority shall give a notice requiring such person to furnish such return before such date as may be specified in the notice.</p>
            
           
        </div>
        <div class="instruction">
            <h2><span> Section 5 </span>  Assessment of cess.</h2>
            <p>(1) The officer or authority to whom or to which the return has been furnished under section 4 shall, after making or causing to be made such inquiry as he or it thinks fit and after satisfying himself or itself that the particulars stated in the return are correct, by order, assess the amount of cess payable by the employer.</p>
            <p>  (2) If the return has not been furnished to the officer or authority under sub-section (2) of section 4, he or it shall, after making or causing to be made such inquiry as he or it thinks fit, by order, assess the amount of cess payable by the employer.</p>
            <p>(3) An order of assessment made under sub-section (1) or sub-section (2) shall specify the date within which the cess shall be paid by the employer.</p> 
           
        </div>
        <div class="instruction">
            <h2><span> Section 6 </span>   Power to exempt.</h2>
            <p>Notwithstanding anything contained in this Act, the Central Government may, by notification in the Official Gazette, exempt any employer or class of employers in a State from the payment of cess payable under this Act where such cess is already levied and payable under any corresponding law in force in that State.</p>
        </div>
        <div class="instruction">
            <h2><span> Section 7 </span> Power of entry.</h2>
            <h4>Any officer or authority of the State Government specially empowered in this behalf by that Government may--</h4>
            <p>(a) with such assistance, if any, as he or it may think fit, enter at any reasonable time any place where he or it considers it necessary to enter for carrying out the purposes of this Act including verification of the correctness of any particulars furnished by any employer under section 4;</p>
            <p>(b) do within such place anything necessary for the proper discharge of his or its duties under this Act; and</p>
            <p>(c) exercise such other powers as may be prescribed.</p>
        </div>
        <div class="instruction">
            <h2><span> Section 8 </span>  Interest payable on delay in payment of cess.</h2>
            <p>If any employer fails to pay any amount of cess payable under section 3 within the time specified in the order of assessment, such employer shall be liable to pay interest on the amount to be paid at the rate of two per cent. for every month or part of a month comprised in the period from the date on which such payment is due till such amount is actually paid.</p>
           
        </div>

