        <div class="body-box">
            <h1>FAQ</h1>
            <div class="faq-box">
                
                <button class="accordion">
                    Q11 What is the rate of levy of Building & Other Construction Workers’ Welfare Cess 
                    [BOCWW Cess]?
                </button>
                <div class="panel">
                    
                    <p>
                        Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] is levied @ 1% on the 
                        cost of construction incurred by an employer as per the provisions of Sections 3(1) and 3(3) 
                        of the Building and Other Construction Workers’ Welfare Cess Act, 1996 read with 
                        Notification No.S.O 2899 dated 26.9.1996.
                    </p>
                    
                </div>
                <button class="accordion">
                    Q12 On what value is this Cess levied?
                </button>
                <div class="panel">
                    
                    <p>
                        The Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] levied and 
                        collected on the cost of construction incurred by employers.
                    </p>
                    
                </div>
                <button class="accordion">
                    Q13 What is meant by the term ‘cost of construction’?
                </button>
                <div class="panel">
                    
                    <p>
                        In exercise of the power conferred under Section 14 of the Building and Other Construction 
                        Workers’ Welfare Cess Act, 1996, the Central Government has framed the Building and 
                        Other Construction Workers’ Welfare Cess Rules, 1998. 
                    </p>
                    <p>
                        While the charging Section 3 of the Building and Other Construction Workers’ Welfare Cess 
                        Act, 1996 uses the expression, “cost of construction”, the same is explained in the Rules. 
                    </p>
                    <p>
                        Thus, the charging Section as well as rule 3 ought to be read together homogenously to sub-serve the object of the enactment.
                    </p>
                    <p>
                        As per Rule 3 of the Building and Other Construction Workers’ Welfare Cess Rules, 1998, 
                        the “cost of construction” is an inclusive definition which includes all expenditure except
                    </p>
                    <p>
                       1) cost of land and
                    </p>
                    <p>
                        2) any compensation paid or payable to a worker or his kin under the Workmen's 
                        Compensation Act, 1923.
                    </p>
                </div>
                <button class="accordion">
                    Q14 The name of the Cess itself is Building & Other Construction Workers’ Welfare Cess 
                    [BOCWW Cess]. So, is this Cess levied on the labour portion of the cost incurred during 
                    a construction?

                </button>
                <div class="panel">
                    
                    <p>
                        The Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] is levied on the 
                        total cost of construction (i.e. the cost comprising of both the materials [supply] and the 
                        labour [service] portion). The definition of cost of construction as per the statute (as already 
                        discussed above) excludes only cost of land and any compensation paid or payable to worker 
                        or his kin under the Workmen’s Compensation Act, 1923.
                    </p>
                    <p>
                        The same has been upheld by the High Court of Madhya Pradesh, Jabalpur in the order dt. 
                        3.5.2012 passed in relation to Writ Petitions vide No. WP-3956/2009 [M/s. Technical 
                        Associates Limited vs The Assistant Labour Commissioner, Jabalpur, and others]; No. WP-14029/2009 [M/s. Star Delta Transformers Limited vs The Assistant Labour Commissioner, 
                        Jabalpur, and others]; No. WP-7303/2011 [M/s. G. V. P. R. Engineers Limited vs State of 
                        Madhya Pradesh and others]; No. WP-3226/2010 [Rohini Industrial Electricals Limited vs 
                        The Assistant Labour Commissioner, Jabalpur, and others]; No. WP-9871/2010 [M/s. Easun 
                        Reyiolle Limited vs The Assistant Labour Commissioner, Jabalpur, and others]; No. WP-15906/2011 [M/s. K. E. I. Industries Limited vs The Assistant Labour Commissioner, 
                        Jabalpur, and others]; No. WP-5173/2011 [M/s. Bajaj Electrical Limited vs The Assistant 
                        Labour Commissioner, Jabalpur, and others]; No. WP-2893/2012 [M/s. Eltel Engineers (JV) 
                        vs The Assistant Labour Commissioner, Jabalpur, and others] where the Hon’ble Court has 
                        stated that 
                    </p>
                    <p>
                        “The cost of construction cannot be divided in parts, as argued by the petitioners, into supply 
                        portion and erection portion. Even the cost for supply portion is incurred by the petitioners and 
                        cannot be separated from the total cost incurred…” 
                    </p>
                </div>
                <button class="accordion">
                    Q15 Why is this cess levied on the entire cost of construction inspite of the nomenclature 
                    based on Labour only? 

                </button>
                <div class="panel">
                    
                    <p>
                        The exception clauses relating to cost of construction as provided in Section 3 of Building 
                        and Other Construction Workers’ Welfare Cess Act, 1996 and Rule 3 of the Building and 
                        Other Construction Workers’ Welfare Cess Rules, 1998 clearly indicate that there was no 
                        intention of the statute to exclude the cost of supply part. 
                    </p>
                    <p>
                        Thus, the Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] is applicable 
                        on the total contract price and thus such cess is levied on both the materials [supply] and 
                        the labour [service] portion.
                    </p>
                    <p>
                        This has also been upheld by the High Court of Madhya Pradesh, Jabalpur in the order dt. 
                        3.5.2012 passed in relation to Writ Petitions vide No. WP-3956/2009 [M/s. Technical 
                        Associates Limited vs The Assistant Labour Commissioner, Jabalpur, and others]; No. WP-14029/2009 [M/s. Star Delta Transformers Limited vs The Assistant Labour Commissioner, 
                        Jabalpur, and others]; No. WP-7303/2011 [M/s. G. V. P. R. Engineers Limited vs State of 
                        Madhya Pradesh and others]; No. WP-3226/2010 [Rohini Industrial Electricals Limited vs 
                        The Assistant Labour Commissioner, Jabalpur, and others]; No. WP-9871/2010 [M/s. Easun 
                        Reyiolle Limited vs The Assistant Labour Commissioner, Jabalpur, and others]; No. WP-15906/2011 [M/s. K. E. I. Industries Limited vs The Assistant Labour Commissioner, 
                        Jabalpur, and others]; No. WP-5173/2011 [M/s. Bajaj Electrical Limited vs The Assistant 
                        Labour Commissioner, Jabalpur, and others]; No. WP-2893/2012 [M/s. Eltel Engineers (JV) 
                        vs The Assistant Labour Commissioner, Jabalpur, and others]
                    </p>
                </div>
                <button class="accordion">
                    Q16 What is the mechanism by which this Building & Other Construction Workers’ Welfare 
                    Cess [BOCWW Cess] is levied? 

                </button>
                <div class="panel">
                    
                    <p>
                        As per the provisions of Section 3(2) of the Building and Other Construction Workers’ 
                        Welfare Cess Act, 1996, Building & Other Construction Workers’ Welfare Cess [BOCWW 
                        Cess] is levied and collected in two ways: 
                    </p>
                    <p>
                        1) By way of deduction at source by Government and PSU, from the cost of construction.
                    </p>
                    <p>
                        2) As an advance collection through a local authority where an approval of such building 
                        or other construction works by such local authority is required.
                    </p>
                </div>
                <button class="accordion">
                    Q17 Is it mandatory for a Govt. Dept. to deduct Building & Other Construction Workers’ 
                    Welfare Cess [BOCWW Cess] at Source? 

                </button>
                <div class="panel">
                    
                    <p>
                        As per the provisions of the Building and Other Construction Workers’ Welfare Cess Act, 
                        1996 read with the Building and Other Construction Workers (Regulation of Employment 
                        and Conditions of Service) Act, 1996, all the Govt. Departments are required to collect 
                        Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] for building & other 
                        construction works of establishments from employer-contractors.
 
                    </p>
                    <p>
                        Rule 4 of the Building and Other Construction Workers’ Welfare Cess Rules, 1998 makes it 
                        mandatory for deduction of cess payable at the notified rates (i.e. @1% as discussed above) 
                        from the bills paid for the building and other construction work of a government or a public 
                        sector undertaking.
                    </p>
                    <p>
                        The procedural parts of such deduction are guided by the provisions of the West Bengal 
                        Building and Other Construction Workers (Regulation of Employment and Conditions of 
                        Service) Rules, 2004.
                    </p>
                </div>
                <button class="accordion">
                    Q18 Is there any collection charge for cess collected?

                </button>
                <div class="panel">
                    
                    <p>
                        Yes. Such government office of Public Sector Undertaking may deduct from the cess
                        collected, actual collection expenses not exceeding one per cent, of the total amount collected 
                        by them.
 
                    </p>
                </div>
                <button class="accordion">
                    Q19 Is there any time limit for transfer of the cess collected to the Welfare Fund of the 
                    Board?

                </button>
                <div class="panel">
                    
                    <p>
                        Yes. The amount collected shall be transferred to the Board within thirty days of its 
                        collection.
 
                    </p>
                </div>
                <button class="accordion">
                    Q20 Is there any statutory provision regarding exemption of levy and collection of Building 
                    & Other Construction Workers’ Welfare Cess [BOCWW Cess]?
                </button>
                <div class="panel">
                    
                    <p>
                        The levy and collection of Building & Other Construction Workers’ Welfare Cess [BOCWW 
                        Cess] is applicable on all buildings or other construction works except on those buildings or 
                        other construction works to which the provisions of the Factories Act, 1948, or the Mines 
                        Act, 1952, apply, vide the provisions of Section 2(d) of the Building and Other Construction 
                        Workers’ Welfare Cess Act, 1996 read with Section 2(1)(d) of the Building and Other 
                        Construction Workers (Regulation of Employment and Conditions of Service) Act, 1996. 
                    </p>
                    <p>
                        However, the Central Government may, by notification in the Official Gazette, exempt any
                        employer or class of employers in a State from the payment of cess payable under this Act 
                        where such cess is already levied and payable under any corresponding law in force in that 
                        State.
                    </p>
                </div>

