            <h1>FAQ</h1>
            <div class="faq-box">
                
                <button class="accordion">
                    Q31 On being deducted, how should the Building & Other Construction Workers’ Welfare 
                    Cess [BOCWW Cess] be transferred to the appropriate Account?
                </button>
                <div class="panel">
                    
                    <p>
                        It is needless to say that any payment made by a WB Govt. Dept. to any beneficiary is done 
                        by raising a Bill in the appropriate TR Form in the e-billing module of IFMS.
                    </p>
                    <p>
                        The respective DDO enters the applicable data pertaining to the respective Bill amount 
                        payable to the beneficiary with inputs of all applicable deductions (e.g. IT-TDS, GST-TDS, 
                        Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] etc) and uploads the 
                        Bill in the IFMS e-billing module. 
                    </p>
                    <p>
                        The Bill then flows to the Treasury and upon approval; the payment (after all deductions) 
                        gets credited to the beneficiary. The transfer of the Building & Other Construction Workers’ 
                        Welfare Cess [BOCWW Cess] so deducted to the appropriate Account is done by the system 
                        itself.  
                    </p>
                    <p>
                        The said fund is set apart and appropriated specifically for the performance of specified 
                        purpose. It is not merged in the tax or non-tax revenues but gets appropriated to a Public 
                        account.
                    </p>
                </div>
                <button class="accordion">
                    Q32 In case a DDO of a WB Govt. Dept. inadvertently misses out in deducting Building & 
                    Other Construction Workers’ Welfare Cess [BOCWW Cess] on an applicable Bill 
                    uploaded in the IFMS, can the system give him any alert for rectification?
                </button>
                <div class="panel">
                    
                    <p>
                        No. Currently, there is no system check for the deduction of this Cess. Hence, deduction of 
                        this Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] currently remains 
                        as the prerogative of the DDO. In case of any error on the part of the DDO, even when 
                        inadvertent in nature, a Bill for payment to a contractor in relation to such construction 
                        works may get approved without deduction of Building & Other Construction Workers’ 
                        Welfare Cess [BOCWW Cess].
                    </p>
                </div>
                <button class="accordion">
                    Q33 Does a DDO deducting Building & Other Construction Workers’ Welfare Cess [BOCWW 
                    Cess] need to file any return showing the data of such deductions?
                </button>
                <div class="panel">
                    
                    <p>
                        No. Section 4 of The Building and Other Construction Workers’ Welfare Cess Act, 1996 
                        requires “every employer” to file returns in the manner prescribed.
                    </p>
                    <p>
                        As already discussed above, as per Section 2(1)(i) of the Building and Other Construction 
                        Workers (Regulation of Employment and Conditions of Service) Act, 1996 read with Section 
                        2(d) of the Building and Other Construction Workers’ Welfare Cess Act, 1996, an “employer” 
                        mostly means a contractor, so far the works undertaken by WB Govt.
                    </p>
                </div>
                <button class="accordion">
                    Q34 In a particular agreement, a Govt. Dept. has entered into an agreement with a 
                    contractor for construction, operation, maintenance and transfer of a road on “Design, 
                    Built, Finance, Operate and Transfer (DBFOT)”. For carrying out the aforesaid 
                    construction of the said road, the main contractor has appointed EPC 
                    Contractors/Sub-Contractors. Shall the provisions of Building & Other Construction 
                    Workers’ Welfare Cess [BOCWW Cess] be applicable on the contractor only or on the 
                    sub-contractors too?
                </button>
                <div class="panel">
                    
                    <p>
                        As already discussed above an “employer” as defined in Section 2(1)(iii) of the Building and 
                        Other Construction Workers (Regulation of Employment and Conditions of Service) Act, 1996 
                        read with Section 2(d) of the Building and Other Construction Workers’ Welfare Cess Act, 
                        1996, in relation to a building or other construction work carried on by or through a 
                        contractor, or by the employment of building workers supplied by a contractor, means the 
                        contractor.
                    </p>
                    <p>
                        Again, as per Section 2(1)(g) of the Building and Other Construction Workers (Regulation of 
                        Employment and Conditions of Service) Act, 1996 read with Section 2(d) of the Building and 
                        Other Construction Workers’ Welfare Cess Act, 1996, the term “contractor” means a person 
                        who undertakes to produce a given result for any establishment, other than a mere supply of 
                        goods or articles of manufacture, by the employment of building workers or who supplies 
                        building workers for any work of the establishment, and includes a sub-contractor.
                    </p>
                </div>
                <button class="accordion">
                    Q35 The levy and collection of Building & Other Construction Workers’ Welfare Cess 
                    [BOCWW Cess] is exempted in case of buildings or other construction works to which 
                    the provisions of the Factories Act, 1948, or the Mines Act, 1952, apply.
                    Does this mean that construction of a factory does not involve levy of Building &
                    Other Construction Workers’ Welfare Cess [BOCWW Cess]? 
                </button>
                <div class="panel">
                    
                    <p>
                        Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] is a means to provide 
                        health and welfare measures for the workers engaged in building and other construction 
                        works. It is levied for building & other construction works of all establishments except on 
                        those buildings or other construction works to which the provisions of the Factories Act, 
                        1948, or the Mines Act, 1952 are applicable.
                    </p>
                    <p>
                        As per Section 2(l) of the Factories Act, 1948, "worker" means a person employed in any 
                        manufacturing process, or in cleaning any part of the machinery or premises used for a 
                        manufacturing process, or in any kind of work incidental to or connected with, the 
                        manufacturing process, or the subject of the manufacturing process.
                    </p>
                    <p>
                        As under Section 2(k) of the Factories Act, 1948, "Manufacturing process" includes the 
                        following:
                    </p>
                    <p>
                        1) Making, altering, repairing, ornamenting, finishing, packing, oiling, washing, cleaning, 
                        breaking up, demolishing, or otherwise treating or adapting any article or substance 
                        with a view to its use, sale, transport, delivery or disposal.
                    </p>
                    <p>
                        2) Pumping oil, water, sewage or any other substance.                    

                    </p>
                    <p>
                        3) Generating, transforming or transmitting power.
                    </p>
                    <p>
                        4) Composing types for printing, printing by letter press, lithography, photogravure or 
                        other similar process or book binding.
                    </p>
                    <p>5) Constructing, reconstructing, repairing, refitting, finishing or breaking up ships or 
                        vessels.
                    </p>
                    <p>
                        6) Preserving or storing any article in cold storage.
                    </p>
                    <p>
                        In view of the above, it is inferred that the term "worker" under the Factories Act, 1948, does 
                        not involve a person involved in the construction of buildings. Also, the term "manufacturing 
                        process" does not involve any process for construction of buildings. Hence, the Factories Act 
                        covers only those workers who are engaged in the manufacturing process or any work that is 
                        incidental to the manufacturing process, and not workers who are involved in building and 
                        construction works.
                    </p>
                    <p>
                        The fact that construction workers are not covered under the Factories Act, 1948, and are 
                        therefore entitled to welfare measures under the Building and Other Construction Workers’ 
                        Welfare Cess Act, 1996 was put forth in the case of Lanco Anpara Power Ltd. V. State Of 
                        Uttar Pradesh & Others dated Oct. 10, 2016.
                    </p>
                    <p>
                        The petitioner construction companies, which are engaged in construction activities, argued 
                        that once the undertaking or the establishment had obtained a license for registration under 
                        the Factories Act, the Building and Other Construction Workers’ Welfare Cess Act, 1996 was 
                        not applicable on account of the exclusion under Section 2(1)(d) of the Building and Other 
                        Construction Workers’ Welfare Cess Act, 1996. 
                    </p>
                    <p>
                        The issue before the Supreme Court was whether the Building and Other Construction 
                        Workers’ Welfare Cess Act, 1996 was applicable to those premises that are registered under 
                        the Factories Act, 1948.
                    </p>
                    <p>
                        The Court held that the establishments/premises registered under the Factories Act, 1948, 
                        employing construction workers in the construction of factory premises were not excluded 
                        from the application of the Building and Other Construction Workers’ Welfare Cess Act, 1996
                        in terms of the exclusion clause. The Court reasoned that an establishment/premise would 
                        become a factory under Section 2(m) of the Factories Act only when the manufacturing 
                        process as defined under Section 2(k) of the Factories Act commenced. 
                    </p>
                    <p>
                        Consequently, the provisions of the Factories Act would apply only when the manufacturing 
                        process commences, and even upon commencement of the manufacturing activity, it only 
                        covered those workers who are engaged in such manufacturing activity under the Factories 
                        Act and not construction workers.
                    </p>
                    <p>
                        Hence, construction activities which are carried out in relation to establishments/registered 
                        premises /licensed under the Factories Act, 1948, would not attract the exclusion clause of 
                        the Building and Other Construction Workers’ Welfare Cess Act, 1996 by itself. Workers 
                        carrying on such activities would be covered under the Building and Other Construction Workers’ Welfare Cess Act, 1996.
                    </p>
                </div>
                <button class="accordion">
                    Q36 The DDO of a WB Govt. Dept. has to process a Bill of payment to a beneficiary 
                    contractor for construction works. The DDO has so many deductions to make while 
                    making the payment - Income Tax deducted at source, GST deducted at source & 
                    Building & Other Construction Workers’ Welfare Cess [BOCWW Cess]. The DDO is 
                    confused. Can all such deductions be made from a single Bill? Or, any one deduction 
                    blocks the occasion of other deductions? In that case, which deduction should get the 
                    priority?
                </button>
                <div class="panel">
                    
                    <p>
                        The deductions of Income Tax deducted at source, GST deducted at source & Building & 
                        Other Construction Workers’ Welfare Cess [BOCWW Cess] are not mutually exclusive in 
                        nature. In case, the said deductions are required to be made on any Bill depending on the 
                        conditions as laid down in the respective statutes, all such deductions have to be made on 
                        the single Bill.                        
                    </p>
                </div>
                <button class="accordion">
                    Q37 Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] is a fee as per the 
                    Court of Law. In that case, is GST applicable on Building & Other Construction 
                    Workers’ Welfare Cess [BOCWW Cess]?
                </button>
                <div class="panel">
                    
                    <p>
                        As per the provisions of Section 15(2)(a) of the Central Goods & Services Tax Act/State 
                        Goods & Services Tax Act, 2017, the value of a supply of goods or services or both includes 
                        any taxes, duties, cesses, fees and charges levied under any law for the time being in force 
                        other than this Act and the Goods and Services Tax (Compensation to States) Act, if charged 
                        separately by the supplier.                      
                    </p>
                    <p>
                        This means that while determining the value of supply on which GST is levied, all taxes, 
                        duties, cesses, fees and charges levied under any law for the time being in force other than 
                        GST & GST compensation cess, if charged separately, are to be taken into account.
                    </p>
                    <p>
                        Now, Building & Other Construction Workers’ Welfare Cess [BOCWW Cess] is actually not 
                        separately charged but is a deduction that is made on the Bill value. So, by default, the Bill 
                        value without the GST will automatically be inclusive of this amount to be deducted as 
                        Building & Other Construction Workers’ Welfare Cess [BOCWW Cess]. This means GST is 
                        charged on Building & Other Construction Workers’ Welfare Cess [BOCWW Cess].
                    </p>
                </div>
                <button class="accordion">
                    Q38 Will TDS under GST be deducted on a Bill before or after deducting Building & Other 
                    Construction Workers’ Welfare Cess [BOCWW Cess]?
                </button>
                <div class="panel">
                    
                    <p>
                        As per the explanation provided to Section 51(1) of the Central Goods & Services Tax 
                        Act/State Goods & Services Tax Act, 2017, for the purpose of TDS under GST, the value of 
                        supply is to be taken as the amount excluding the components of GST and GST cess 
                        indicated in the invoice.
                    
                    </p>
                    <p>
                        Thus, TDS under GST is be deducted on a Bill only after excluding the GST components 
                        (including GST cess) and not on the value after deduction of the Building & Other Construction Workers’ Welfare Cess [BOCWW Cess]. 
                    </p>
                    
                </div>
                <button class="accordion">
                    Q39 Is there any provision of penalty under Building & Other Construction Workers’ 
                    Welfare Cess [BOCWW Cess] Act?
                </button>
                <div class="panel">
                    
                    <p>
                        Yes. An Assessing Officer, shall impose a penalty upto such amount of cess which an 
                        employer :                  
                    </p>
                    <p>1) has not paid within the date as specified in the assessment order</p>
                    <p>2) has less paid, including</p>
                    <p>	&#10003 the cess deducted at source or</p>
                    <p>	&#10003 paid in advance</p>
                    
                </div>
                <button class="accordion">
                    Q40 Does a Govt. DDO have any liability to pay interest or penalty for non-deduction or 
                    short deduction of BOCWW Cess?
                </button>
                <div class="panel">
                    
                    <p>
                        Under the BOCWW Act & Rules, the employer is responsible for non-deduction/short 
                        deduction of cess.                 
                    </p>
                    <p>
                        If the DDO is also an employer by nature, as provisions of Section 2(1)(i) of the Building and 
                        Other Construction Workers (Regulation of Employment and Conditions of Service) Act, 1996 
                        read with Section 2(d) of the Building and Other Construction Workers’ Welfare Cess Act, 
                        1996, already discussed above, he will also stand responsible.
                    </p>
                    <p>
                        However, if the DDO is only making a payment to the contractor, he has no such liability to 
                        pay any penalty for non-deduction/short deduction of cess.
                    </p>
                    
                </div>

